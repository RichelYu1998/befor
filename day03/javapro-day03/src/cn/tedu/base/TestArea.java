package cn.tedu.base;

public class TestArea {
	public static void main(String[] args) {
		byte b = 10;
		short s = 500;
		int i = 9093838;
		long x = 2147483648L;	//加后缀，告诉编译器这不是整型是长整型
		
		float f = 1.2F;					//告诉编译器这个值是一个单精度浮点数
		double d = 1.2D;			//告诉编译器这个值是一个双精度浮点数
		
		
		char c = 'a';
		boolean bn = false;		//就两个值true/false
	}
}
